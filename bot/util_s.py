            total_len = len(key) + len(val)  # –í—ã—á–∏—Å–ª—è–µ–º –æ–±—â—É—é –¥–ª–∏–Ω—É –∫–ª—é—á–∞ –∏ –∑–Ω–∞—á–µ–Ω–∏—è
